#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int unsigned	i;

	if (f != NULL && s != NULL)
	{
		i = 0;
		while (s[i])
		{
			(*f)(i, &s[i]);
			i++;
		}
	}
}
